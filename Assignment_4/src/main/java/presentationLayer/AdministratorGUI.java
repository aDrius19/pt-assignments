    private void createBaseProductMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_createBaseProductMouseClicked
        String name = baseProductName.getText();
        double price = Double.parseDouble(baseProductPrice.getText());
        if(!isStringOnlyAlphabet(name, 0) || (price == 0 || price < 0)) {JOptionPane.showMessageDialog(null, "No correct input!", "Error", JOptionPane.ERROR_MESSAGE, null);}
        else{
        MenuItem item = new MenuItem(name, price);
        restaurant.addMenuItem(item);
        JOptionPane.showMessageDialog(null, "Created 1 BASE PRODUCT", "UI Info", JOptionPane.INFORMATION_MESSAGE, null);
        RestaurantSerializator.serialize(restaurant);
        }
    }//GEN-LAST:event_createBaseProductMouseClicked

    private CompositeProduct compItem = new CompositeProduct();
    private void addComponentMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addComponentMouseClicked
        String name = compositeProductName.getText();
        if(!isStringOnlyAlphabet(name, 0)) {JOptionPane.showMessageDialog(null, "No correct input!", "Error", JOptionPane.ERROR_MESSAGE, null);}
        else{
        compItem.setName(name);
        String compName = compositeComponent.getText();
        MenuItem item = restaurant.findMenuItem(compName);
        if (item != null) {
            compItem.addCompItem(item);
            JOptionPane.showMessageDialog(null, "Created 1 COMPONENT", "UI Info", JOptionPane.INFORMATION_MESSAGE, null);
        } else {
            JOptionPane.showMessageDialog(null, "There is no menu item having the name: " + compName, "ErrorCompItem", JOptionPane.WARNING_MESSAGE, null);
        }

        }
    }//GEN-LAST:event_addComponentMouseClicked

    private void createCompositeProductMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_createCompositeProductMouseClicked
        compItem.setPrice(compItem.computePrice());
        restaurant.addMenuItem(compItem);
        JOptionPane.showMessageDialog(null, "Created 1 COMPOSITE PRODUCT", "UI Info", JOptionPane.INFORMATION_MESSAGE, null);
        compItem = null;
        RestaurantSerializator.serialize(restaurant);
    }//GEN-LAST:event_createCompositeProductMouseClicked

    private void editMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editMouseClicked
        String name = editNameField.getText();
        double price = Double.parseDouble(newPrice.getText());
        if(!isStringOnlyAlphabet(name, 0) || (price == 0 || price < 0)) {JOptionPane.showMessageDialog(null, "No correct input!", "Error", JOptionPane.ERROR_MESSAGE, null);}
        else{MenuItem item = new MenuItem();
        item.setName(name);
        restaurant.editMenuItem(item, price);
        JOptionPane.showMessageDialog(null, "Edited 1 PRODUCT", "UI Info", JOptionPane.INFORMATION_MESSAGE, null);
        RestaurantSerializator.serialize(restaurant);}
    }//GEN-LAST:event_editMouseClicked

    private void deleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteMouseClicked
        String name = deleteField.getText();
        if(!isStringOnlyAlphabet(name, 0)) {JOptionPane.showMessageDialog(null, "No correct input!", "Error", JOptionPane.ERROR_MESSAGE, null);}
        else{restaurant.removeMenuItem(name);
        JOptionPane.showMessageDialog(null, "Deleted 1 PRODUCT", "UI Info", JOptionPane.INFORMATION_MESSAGE, null);
        RestaurantSerializator.serialize(restaurant);}
    }//GEN-LAST:event_deleteMouseClicked

    private void viewMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_viewMouseClicked
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        for (MenuItem item : restaurant.getItems()) {
            Object[] row = { item.getName(), item.computePrice()};
            model.addRow(row);
            jTable1 = new JTable(model);
            jScrollPane1.add(jTable1);
        }
    }//GEN-LAST:event_viewMouseClicked

