    public static boolean isStringOnlyAlphabet(String str, int ok) //validations for the inputs using regex
    { 
        if(ok == 0) 
        {return ((str != null) 
                && (!str.equals("")) 
                && (str.matches("^[a-zA-Z]*$")));
        }
        if(ok == 1){
            return ((str != null) 
                && (!str.equals("")) 
                && (str.matches("^[0-9]*$")));}
        if(ok == 2){
            return ((str != null) 
                && (!str.equals("")) 
                && (str.matches("^(0[1-9]|[12][0-9]|3[01])[- /.](0[1-9]|1[012])[- /.](19|20)\\d\\d$")));}
        return true;
    }

